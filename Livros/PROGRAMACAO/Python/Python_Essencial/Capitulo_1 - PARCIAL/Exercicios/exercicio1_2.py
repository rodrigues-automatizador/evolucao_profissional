# Crie um programa que armazene dois números em variáveis separadas
# e imprima a soma, subtração, multiplicação e divisão desses números

valor1 = 10
valor2 = 35

print(f"A soma do valor: {valor1} + {valor2} é:", valor1 + valor2)
print(f"A subtração do valor: {valor1} - {valor2} é:", valor1 - valor2)
print(f"A multiplicação do valor: {valor1} x {valor2} é:", valor1 * valor2)
print(f"A divisão do valor: {valor1} / {valor2} é:", valor1 / valor2)