# Crie um programa que peça ao usuário para digitar um número.
# Em seguida, o programa deve calcular e mostrar a raiz quadrada desse número.

raiz_quadrada = int(input("Digite um número para calcular a raiz quadrada: "))
resultado = raiz_quadrada ** 0.5
print(f"A raiz quadrada de {raiz_quadrada} é: {resultado}")