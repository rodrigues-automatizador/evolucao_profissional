# inteiro para texto
valorInteiro = 42
valorTexto = str(valorInteiro)

print("Número:", valorInteiro, "tipo:", type(valorInteiro))
print("Texto:", valorTexto, "tipo:", type(valorTexto))

# texto para inteiro
valorTexto = "42"
valorInteiro = int(valorTexto)

print("Texto:", valorTexto, "tipo:", type(valorTexto))
print("Número:", valorInteiro, "tipo:", type(valorInteiro))

# real para texto
valorReal = 3.14
valorTexto = str(valorReal)

print("Real:", valorReal, "tipo:", type(valorReal))
print("Texto:", valorTexto, "tipo:", type(valorTexto))

# texto para real
