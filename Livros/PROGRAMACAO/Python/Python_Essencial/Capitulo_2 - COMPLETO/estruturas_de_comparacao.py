# Verificação de igualdade

a = 5
b = 5

if a == b:
    print("a é igual a b.")
    
else:
    print("a é diferente de b.")
    
# Verificação de diferença
a = 5
b = 3

if a != b:
    print("a é diferente de b.")

else:
    print("a é igual a b")
    
    
# Verificação de número par ou ímpar
numero = 4

if numero % 2 == 0:
    print(numero, "é par.")

else:
    print(numero, "é ímpar.")
