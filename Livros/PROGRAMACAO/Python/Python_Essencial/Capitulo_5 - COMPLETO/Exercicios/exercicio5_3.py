# Crie um programa que tenha 3 parâmetros: um número inteiro, um número real e um texto. 
# A função deve imprimir os valores dos parâmetros na tela. 
# Em seguida, chame a função passando os argumentos de forma posicional

def funcao(numero_inteiro, numero_real, texto):
    print(f"Valor do número inteiro: {numero_inteiro}")
    print(f"Valor do número real: {numero_real}")
    print(f"Valor do text: {texto}")
    
funcao(10, 11.2, "testando a função!")